export type BorderTheme = '8bit' | 'japanese' | 'anime' | 'cyberpunk' | 'gothic' | 'kawaii' | 'elemental' | 'cosmic' | 'special';
export type BorderRarity = 'free' | 'common' | 'rare' | 'epic' | 'legendary' | 'mythic';
export type BorderAnimationType = 
  | 'none' | 'pulse' | 'glow' | 'rotate' | 'shimmer' | 'rainbow'
  | 'fire' | 'ice' | 'electric' | 'void' | 'aurora' | 'galaxy'
  | 'pixel-pulse' | 'scan-line' | 'glitch' | 'sakura-fall' | 'wave'
  | 'energy-surge' | 'smoke' | 'neon-flicker' | 'holographic';

export interface BorderDefinition {
  id: string;
  name: string;
  theme: BorderTheme;
  rarity: BorderRarity;
  animationType: BorderAnimationType;
  colors: string[];
  isPremium: boolean;
  unlocked: boolean;
  unlockRequirement?: string;
  unlockLevel?: number;
  description: string;
  borderWidth?: number;
  glowIntensity?: number;
  animationDuration?: number;
  /** URL to the Lottie JSON animation for this border (served from /public/lottie/borders/). */
  lottieFile?: string;
}

export interface BorderThemeCategory {
  id: BorderTheme;
  name: string;
  description: string;
  icon: string;
  accentColor: string;
  borderCount: number;
}


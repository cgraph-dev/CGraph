/**
 * Animation types that are rendered exclusively via Lottie (no CSS fallback).
 * Only borders with these types get auto-assigned Lottie files.
 * Borders with CSS-rendered types (pulse, glow, fire, etc.) keep their CSS effects.
 */
const LOTTIE_ONLY_TYPES = new Set<string>([
  'holographic', 'galaxy', 'pixel-pulse', 'scan-line', 'glitch',
  'sakura-fall', 'wave', 'energy-surge', 'smoke', 'neon-flicker', 'rainbow',
]);

/** All borders — Lottie URLs only assigned to borders with Lottie-only animation types. */
export const ALL_BORDERS: BorderDefinition[] = RAW_BORDERS.map((border) => {
  // Borders that already have lottieFile set directly (e.g. Avatar Frame) keep it
  if (border.lottieFile) return border;
  // Only assign Lottie to animation types without CSS implementations
  if (!LOTTIE_ONLY_TYPES.has(border.animationType)) return border;
  const lottieUrl = getBorderLottieUrl(border.theme, border.rarity);
  return lottieUrl ? { ...border, lottieFile: lottieUrl } : border;
});

// Get borders by theme
export const getBordersByTheme = (theme: BorderTheme): BorderDefinition[] => {
  return ALL_BORDERS.filter(border => border.theme === theme);
};

// Get borders by rarity
export const getBordersByRarity = (rarity: BorderRarity): BorderDefinition[] => {
  return ALL_BORDERS.filter(border => border.rarity === rarity);
};

// Get border by ID
export const getBorderById = (id: string): BorderDefinition | undefined => {
  return ALL_BORDERS.find(border => border.id === id);
};


/** Quick reactions for message actions */
export const QUICK_REACTIONS = ['❤️', '😂', '😮', '😢', '😡', '👍'];

export interface UseReactionsReturn {
  // State
  showReactionPicker: boolean;
  reactionPickerMessage: Message | null;
  selectedEmojiCategory: keyof typeof EMOJI_CATEGORIES;
  // Actions
  openReactionPicker: (message: Message) => void;
  closeReactionPicker: () => void;
  setSelectedEmojiCategory: (category: keyof typeof EMOJI_CATEGORIES) => void;
  // Helpers
  hasReacted: (message: Message, emoji: string) => boolean;
}

/**
 * Hook for managing reaction picker state and behavior.
 */
export function useReactions(): UseReactionsReturn {
  const [showReactionPicker, setShowReactionPicker] = useState(false);
  const [reactionPickerMessage, setReactionPickerMessage] = useState<Message | null>(null);
  const [selectedEmojiCategory, setSelectedEmojiCategory] =
    useState<keyof typeof EMOJI_CATEGORIES>('Smileys');

  /**
   * Open the reaction picker for a message.
   */
  const openReactionPicker = useCallback((message: Message) => {
    Haptics.impactAsync(Haptics.ImpactFeedbackStyle.Light);
    setReactionPickerMessage(message);
    setShowReactionPicker(true);
  }, []);

  /**
   * Close the reaction picker.
   */
  const closeReactionPicker = useCallback(() => {
    setShowReactionPicker(false);
    setReactionPickerMessage(null);
  }, []);

  /**
   * Check if user has already reacted with an emoji.
   */
  const hasReacted = useCallback((message: Message, emoji: string): boolean => {
    return message.reactions?.some((r) => r.emoji === emoji && r.hasReacted) || false;
  }, []);

  return {
    // State
    showReactionPicker,
    reactionPickerMessage,
    selectedEmojiCategory,
    // Actions
    openReactionPicker,
    closeReactionPicker,
    setSelectedEmojiCategory,
    // Helpers
    hasReacted,
  };
}

export default useReactions;
